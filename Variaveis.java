public class Variaveis {
    public static void main(String[] args) throws Exception {
        int varInt = 2; // declaração da variável varInt, do tipo int, com valor recebido de: 2
        System.out.println("Variável do tipo int (inteiro): " + varInt);

        Integer varInteger = 3; // declaração da variável varInteger, do tipo Integer, com valor recebido de: 3
        System.out.println("Variável do tipo Integer (inteiro): " + varInteger);

        String varString = "texto"; // declaração da variável varString, do tipo String, com valor recebido de: texto
        System.out.println("Variável do tipo String (texto): " + varString);

        boolean varBoolean = true; // declaração da variável varBoolean, do tipo Boolean, com valor recebido de: true
        System.out.println(varBoolean);

        byte varByte = 1; // declaração da variável varByte, do tipo Byte, com valor recebido de: 1
        System.out.println(varByte);

        char varChar = 'r'; // declaração da variável varChar, do tipo Char, com valor recebido de: r
        System.out.println(varChar);

        float varFloat = 19.99f; // declaração da variável varFloat, do tipo Float, com valor recebido de: 19.99f
        System.out.println(varFloat);

        double varDouble = 0.263568954; //declaração da variável varDouble, do tipo Double, com valor recebido de: 0.263568954
        System.out.println(varDouble);
    }
}